package jing.model.salary;

public class SalaryType {
	private String month;
	private int type;
	private String typeName;
	private String chart;
	private String reverseChar;
	private String text;
	private String assignment;
	private String secondAssignment;

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public String getChart() {
		return chart;
	}

	public void setChart(String chart) {
		this.chart = chart;
	}

	public String getReverseChar() {
		return reverseChar;
	}

	public void setReverseChar(String reverseChar) {
		this.reverseChar = reverseChar;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getAssignment() {
		return assignment;
	}

	public void setAssignment(String assignment) {
		this.assignment = assignment;
	}

	public String getSecondAssignment() {
		return secondAssignment;
	}

	public void setSecondAssignment(String secondAssignment) {
		this.secondAssignment = secondAssignment;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

}
